class Solution
{
    public:
    //Function to check if a string is Isogram or not.
    bool isIsogram(string s)
    {
        unordered_set<char> set;
        for(char ch:s){
            if(set.find(ch)!=set.end()) return false;
            set.insert(ch);
        }
        return true;
    }
};

